package Basics_Java;

public class Lab03_allOpeartors {
    public static void main(String[] args) {

        int a=10;
        int b=1;
        System.out.println(a+b);
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        /// compound opearator
        int age=10;
        age+=10;
        System.out.println(age);
        age-=20;
        System.out.println(age);

    }
}
